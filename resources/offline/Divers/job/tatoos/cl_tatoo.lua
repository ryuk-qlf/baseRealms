							Wait(7000)
							DeleteObject(Jointsupp)
							DetachEntity(Jointsupp, 1, true)
							ClearPedTasksImmediately(PlayerPedId())
							TriggerServerEvent("Validate", nearByPlayer, Tatoos.CatSelected, Tatoos.Iterator)
							Tatoos.Achat.Closable = true
                            InAction = false
						end
					})
				end)
                Wait(1)
            end
        end)
    end
end

