										TriggerServerEvent("SVDrawTattoo", nearByPlayer, Tatoos.CatSelected, Tatoos.Iterator)
										LastTatoos = Tatoos.Iterator
									end
                                end
                            }, Tatoos.Achat)
                        end
                    end
                end)
				RageUI.IsVisible(Tatoos.Achat, function()
					RageUI.Button("Dessiner le tatoo", nil, {RightLabel = "→"}, not InAction, {
						onSelected = function()
                            InAction = true
							Tatoos.Achat.Closable = false
							RequestAnimDict('anim@amb@clubhouse@tutorial@bkr_tut_ig3@')
							while not HasAnimDictLoaded('anim@amb@clubhouse@tutorial@bkr_tut_ig3@') do
							Citizen.Wait(0)
							end
							local x,y,z = table.unpack(GetEntityCoords(PlayerPedId()))
							local prop_name = "v_ilev_ta_tatgun"
							Jointsupp = CreateObject(GetHashKey(prop_name), x, y, z,  true,  true, true)
							AttachEntityToEntity(Jointsupp, PlayerPedId(), GetPedBoneIndex(PlayerPedId(), 28422), -0.0, 0.03, 0, 0, -270.0, -20.0, true, true, false, true, 1, true)
